            <p>What an honor to be among the winners of this year’s VR Fest!  Below, director Mimi Lee receives the award in Las Vegas during CES 2016.</p>
<p><img src="../perch/resources/vrfest-2.jpg" alt="VR Fest 2016" /></p>
<h4>A VR EXPERIMENT</h4>
<p>How do the conventions of music video production translate to VR? To explore that question, we teamed up with J.Lo’s choreographer, Kyle Hanagami, to create this story of a relationship in flux. Our dancers trained for weeks, perfecting every step. Locations, being particularly important in VR, played a central role in deepening our narrative. In the edit, our director, Mimi Lee, navigated a path between contemporary music video editing and observance of the emerging rules of the VR language. <a href="../work/sia-alive-360-dance-video.html" title="See it here!">See it here!</a></p>
<p><img src="../perch/resources/1240183610966283703708248365680315400682771o-1.jpg" alt="Trainrobber at VR Fest 2016" /> </p>
<p>Our friends at Littlstar, after seeing an early cut of the video, gave us a front page launch on their site and app, driving thousands of views within a week of the video’s launch. Simultaneously, Kyle Hanagami promoted the video on his popular YouTube channel, quickly amassing a huge response on the platform. </p>
